package jamder.norms;

public enum CompositeActionType {
	ENTITY_READ("Entity Read"),
	ENTITY_UPDATE("Entity Update"),
	ENTITY_FULL_ACCESS("Entity Full Access"),
	ATTRIBUTE_FULL_ACCESS("Attribute Full Access"),
	PLAN_EXECUTE("Plan Execute"),
	PLAN_FULL_ACCESS("Plan Full Access"),
	ORGANIZATION_UPDATE("Organization Update"),
	ORGANIZATION_FULL_ACCESS("Organization Full Access"),
	ENVIRONMENT_UPDATE("Environment Update"),
	ENVIRONMENT_FULL_ACCESS("Environment Full Access"),
	MESSAGE_FULL_ACCESS("Message Full Access"),
	PROTOCOL_FULL_ACCESS("Protocol Full Access"),
	PROTOCOL_RECEIVE("Protocol Receive"),
	PROTOCOL_SEND("Protocol Send"),
	ROLE_FULL_ACCESS("Role Full Access"),
	ROLE_UPDATE("Role Update"),
	AGENT_UPDATE("Agent Update"),
	AGENT_FULL_ACCESS("Agent Full Access"),
	BELIEF_FULL_ACCESS("Belief Full Access"),
	GOAL_FULL_ACCESS("Goal Full Access");
	
	private String status;
	
	CompositeActionType (String status) {
		this.status = status;
	}

	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}